package de.l3s.eventkg.pipeline.output;

import de.l3s.eventkg.util.FileName;

public enum RDFWriterName {

	ENTITIES(FileName.ALL_TTL_ENTITIES, FileName.ALL_TTL_ENTITIES_PREVIEW),
	EVENTS(FileName.ALL_TTL_EVENTS, FileName.ALL_TTL_EVENTS_PREVIEW),
	TEXT_EVENTS(FileName.ALL_TTL_TEXT_EVENTS, FileName.ALL_TTL_TEXT_EVENTS_PREVIEW),
	EVENT_BASE_RELATIONS(FileName.ALL_TTL_EVENTS_BASE_RELATIONS, FileName.ALL_TTL_EVENTS_BASE_RELATIONS_PREVIEW),
	ENTITY_BASE_RELATIONS(FileName.ALL_TTL_ENTITY_BASE_RELATIONS, FileName.ALL_TTL_ENTITY_BASE_RELATIONS_PREVIEW),
	LITERAL_RELATIONS(FileName.ALL_TTL_EVENT_LITERALS_RELATIONS, FileName.ALL_TTL_EVENT_LITERALS_RELATIONS_PREVIEW),
	EVENT_RELATIONS(FileName.ALL_TTL_EVENTS_OTHER_RELATIONS, FileName.ALL_TTL_EVENTS_OTHER_RELATIONS_PREVIEW),
	TEMPORAL_ENTITY_RELATIONS(
			FileName.ALL_TTL_ENTITIES_TEMPORAL_RELATIONS,
			FileName.ALL_TTL_ENTITIES_TEMPORAL_RELATIONS_PREVIEW),
	ENTITY_RELATIONS(FileName.ALL_TTL_ENTITIES_OTHER_RELATIONS, FileName.ALL_TTL_ENTITIES_OTHER_RELATIONS_PREVIEW),
	TYPES(FileName.ALL_NQ_TYPES, FileName.ALL_NQ_TYPES_PREVIEW),
	VOID(FileName.ALL_TTL_VOID, null),
	INTEGRATED_TYPES(FileName.ALL_NQ_TYPES, FileName.ALL_NQ_TYPES_PREVIEW),
	WIKIDATA_TYPES(FileName.ALL_TTL_TYPES_WIKIDATA, FileName.ALL_TTL_TYPES_WIKIDATA_PREVIEW, false),
	WIKIDATA_TYPE_LABELS(FileName.ALL_TTL_TYPE_LABELS_WIKIDATA, FileName.ALL_TTL_TYPE_LABELS_WIKIDATA_PREVIEW, false),
	DBPEDIA_TYPES(FileName.ALL_TTL_TYPES_DBPEDIA, FileName.ALL_TTL_TYPES_DBPEDIA_PREVIEW, false),
	DBPEDIA_TYPE_LABELS(FileName.ALL_TTL_TYPE_LABELS_DBPEDIA, FileName.ALL_TTL_TYPE_LABELS_DBPEDIA_PREVIEW),
	DBPEDIA_ONTOLOGY(FileName.ALL_TTL_DBPEDIA_ONTOLOGY, FileName.ALL_TTL_DBPEDIA_ONTOLOGY_PREVIEW),
	EVENT_FIRST_SENTENCES(FileName.ALL_TTL_EVENTS_FIRST_SENTENCES, FileName.ALL_TTL_EVENTS_FIRST_SENTENCES_PREVIEW),
	EVENT_DESCRIPTIONS_FROM_TEXTUAL_EVENTS(
			FileName.ALL_TTL_EVENT_DESCRIPTIONS_FROM_TEXTUAL_EVENTS,
			FileName.ALL_TTL_EVENT_DESCRIPTIONS_FROM_TEXTUAL_EVENTS_PREVIEW),
	EVENT_LINK_COUNTS(FileName.ALL_TTL_EVENTS_LINK_RELATIONS, FileName.ALL_TTL_EVENTS_LINK_RELATIONS_PREVIEW, false),
	ENTITY_LINK_COUNTS(FileName.ALL_TTL_ENTITY_LINK_RELATIONS, FileName.ALL_TTL_ENTITY_LINK_RELATIONS_PREVIEW, false),
	EVENT_MENTION_COUNTS(FileName.ALL_TTL_EVENTS_MENTIONS, FileName.ALL_TTL_EVENT_MENTIONS_PREVIEW, false),
	ENTITY_MENTION_COUNTS(FileName.ALL_TTL_ENTITY_MENTIONS, FileName.ALL_TTL_ENTITY_MENTIONS_PREVIEW, false),
	TEXT_EVENT_LINK_COUNTS(FileName.ALL_TTL_TEXT_EVENTS_LINKS, FileName.ALL_TTL_TEXT_EVENTS_LINKS_PREVIEW, false);

	private FileName fileName;
	private FileName fileNamePreview;
	private boolean hasLightFile = true;

	private RDFWriterName(FileName fileName, FileName fileNamePreview) {
		this.fileName = fileName;
		this.fileNamePreview = fileNamePreview;
		this.hasLightFile = true;
	}

	private RDFWriterName(FileName fileName, FileName fileNamePreview, boolean hasLightFile) {
		this.fileName = fileName;
		this.fileNamePreview = fileNamePreview;
		this.hasLightFile = hasLightFile;
	}

	public FileName getFileName() {
		return fileName;
	}

	public FileName getFileNamePreview() {
		return fileNamePreview;
	}

	public boolean hasLightFile() {
		return hasLightFile;
	}

}
